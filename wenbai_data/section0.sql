【阿罗汉】：为“声闻四果”之一，如来十号之一。又作“阿卢汉”、“阿罗诃”、“阿啰呵”、“阿黎呵”、“遏啰曷帝”。略称“罗汉”、“啰呵”。意译“应”、“应供”、“应真”、“杀贼”、“不生”、“无生”、“无学”、“真人”。其中主要的三种意义是应供、杀贼、无生，“应供”意为阿罗汉是真正应该接受人天供养的圣者。“杀贼”，贼，指烦恼贼，证到阿罗汉的圣果，已经把所有的烦恼都断尽了。“无生”，阿罗汉既然断除了一切的烦恼，也断尽了一切染污的行为，这时候已经了生脱死，不会再来受生了，所以称为“无生”。总之，阿罗汉指断尽三界见、思之惑，证得尽智，而堪受世间大供养之圣者。此果位通于大、小二乘，然一般皆作狭义之解释，专指小乘佛教中所得之最高果位而言。若广义言之，则泛指大、小乘佛教中之最高果位。",
