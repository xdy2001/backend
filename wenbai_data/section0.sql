,四十二章经-3,4,心不系道，亦不结业。,心不系于通达解脱的行持之道上，也不结烦恼诸业。,"【道】：即至目的地之通路，或指踏行之道（轨路）。据《俱舍论》卷二十五谓，道即通往涅槃（菩提）之路，为求涅槃果之所依。准此，道乃意谓达成佛教终极目的之修行法则。广义而言，亦指趣向果之通路。例如《大智度论》卷八十四谓，有人天、声闻、缘觉、菩萨等四种道，人、天以“十善”、布施为道，而求世间之福乐；二乘以“三十七道品”为道，而求涅槃；菩萨以“三十七道品”、“六波罗蜜”为道，而求佛果。
【业】：音译“羯磨”。最早见于印度的古奥义书，是婆罗门教、耆那教、生活派（邪命外道）等都袭用的术语。佛教中一般解释为造作。人的身、口、意造作善法与不善法，名为身业、口（语）业、意业。业生灭相续，必感苦乐等果，果是业果，结果的因谓之业因。业虽由人的身、口、意所造，但受烦恼的支配。《大智度论》卷九十四称：“烦恼因缘，故起诸业。”这样就构成惑（烦恼）、业、苦（果）之间的因果关系。《大毗婆沙论》卷一百二十三说：业有作用（语业）、行动（身业）、造作（意业）三义。此句是说心已证道，心外无道可系，见道须陀洹果对治烦恼虽然没有除尽，但由烦恼所起招生死业已经不起，到四果阿罗汉始断尽于烦恼诸业，因此称为“不结业”。",
