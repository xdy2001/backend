,四十二章经-2,1,佛言：“辞亲出家，识心达本，解无为法，名曰沙门。,佛陀世尊说：“辞别亲人出家，识自心源，通达佛的深理，了达一切法的本来真实相，解无为法，这样的行者称为沙门。,"【辞亲】：辞，就是辞别。亲，就是父母亲或亲戚朋友，辞亲目的是要出家。辞亲而出家，可以分为两方面一、辞别亲人，奉父母命出家。佛在世的时候，一个人想要出家，必须经过父母的同意，才可以出家；要是父母反对的话，释迦牟尼佛也不会接受他出家的。二、辞离亲族，以便断除缠累。家庭是一种拖累障碍道业，远离家庭恩爱，才能够修学佛法。出家定义有广义和狭义两种。狭义，就是出离家庭，到寺庙过生活。广义，出世俗家，入真谛家；就是出离了世俗之家，出五蕴家，入法身家。
【无为】：无造作之意。为“有为”之对称。即非由因缘所造作，离生灭变化而绝对常住之法。又作“无为法”。原系涅槃之异名，后世更于涅槃以外立种种无为，于是产生“三无为”、“六无为”、“九无为”等诸说。于小乘各部派中，说一切有部立择灭无为、非择灭无为、虚空无为，合为“三无为”。大众部、一说部、说出世部于“三无为”之外，立空无边处、识无边处、无所有处、非想非非想处等“四无色处”，及缘起支性（十二缘起之理）、圣道支性（八圣道之理）等，总为“九无为”。化地部则以不动、善法真如、不善法真如、无记法真如取代“四无色处”，亦作“九无为”之说。大乘唯识家于“三无为”外，别立不动、想受灭、真如，合为“六无为”；或开立真如为善法、不善法、无记法，而为“八无为”。然无论开立为“六无为”或“八无为”，非谓无为有多种别体，而系断除我、法二执所显之一种法性；复以此一法性从所显之诸缘而称种种之名。准此而言，真如、法性、法界、实相等亦皆为无为法。又以涅槃而言，上记“三无为”中之择灭无为、“六无为”中之真如无为即涅槃；而涅槃乃一切无为法中之最殊胜者。
【沙门】：又作“沙门那”、“沙闻那”、“娑门”、“桑门”、“丧门”。意译“勤劳”、“功劳”、“劬劳”、“勤恳”、“静志”、“净志”、“息止”、“息心”、“息恶”、“勤息”、“修道”、“贫道”、“乏道”。为“出家者”之总称，通于内、外二道。亦即指剃除须发，止息诸恶，善调身心，勤行诸善，期以行趣涅槃之出家修道者。在印度，不单是佛教的出家人叫沙门，在当时的婆罗门教，以及其他九十六种外道，只要是宗教师，都称为“沙门”。不过，释迦牟尼佛的弟子，是“释种沙门”。",
