,四十二章经-1,1,世尊成道已，作是思惟：,佛陀世尊圆满觉悟成道后，作这样的思维：,"【世尊】：如来十号之一。即为世间所尊重者之意，亦指世界中之最尊者。亦直译作“有德”、“有名声”等。这里所称的“世尊”是指释迦牟尼佛。“如来、应供、正遍知、明行足、善逝、世间解、无上士、调御丈夫、天人师、佛世尊”，这是佛的十个通号。在印度，一般用为对尊贵者之敬称，并不限用于佛教；若于佛教，则特为佛陀之尊称。成道：“成佛得道”之略称，即完成佛道之意。又作“成佛”、“得佛”、“得道”、“成正觉”。即菩萨完成修行，成就佛果。成是成就的意思。道，指佛道。
【思惟】：考虑、思索、入定。思考真实之道理，称为“正思惟”，系“八正道”之一；反之，则称“邪思惟”（不正思惟），乃“八邪”之一。",
,四十二章经-1,2,离欲寂静，是最为胜；,离欲寂静，这是最为殊胜的；,【离欲】：欲是欲望，情欲、食欲、淫欲。所谓“五欲”乃财、色、名、食、睡。离欲是离开欲，离开贪欲淫欲，即把“五欲”打破，不为它所缠缚，获得自在。寂静：指心凝住一处之平等安静状态。远离本能所起的精神动摇，称为“寂”；断绝一切感觉苦痛之原因而呈现安静之状态，称为“静”。盖由修禅定，可令心止于一处、远离散乱等。也指涅槃之寂灭无相。,
