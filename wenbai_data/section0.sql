,四十二章经-43,10,视禅定，如须弥柱。,看禅定，不过就如四宝四微合成须弥山的柱子一样。,【须弥】：即须弥山，意译作“妙高山”、“好光山”、“好高山”、“善高山”、“善积山”、“妙光山”、“安明由山”。原为印度神话中之山名，佛教之宇宙观沿用之，谓其为耸立于一小世界中央之高山。以此山为中心，周围有八山、八海环绕，而形成一世界（须弥世界）。此山为世界中心之山，不易动摇，以此比喻禅定。,
,四十二章经-43,11,视涅槃，如昼夕寤。,看涅槃，就如白天黑夜都醒着一样。,【涅槃】：意译为“灭”、“灭度”、“寂灭”、“安乐”、“无为”、“不生”、“解脱”、“圆寂”。涅槃原意是火的熄灭或风的吹散状态。佛教产生以前就有这个概念；佛教用以作为修习所要达到的最高理想境界。含义多种：息除烦恼业因，灭掉生死苦果，生死因果都灭，而人得度，故称“灭”或“灭度”；众生流转生死，皆由烦恼业因，若息灭了烦恼业因，则生死苦果自息，名为“寂灭”或“解脱”；永不再受三界生死轮回，故名“不生”；惑无不尽，德无不圆，故又称“圆寂”；达到安乐无为，解脱自在的境界，称为“涅槃”。,
,四十二章经-43,12,视倒正，如六龙舞。,看倒正，就如六龙舞动一样。,"【倒正】：众生以妄心分别计度故有“倒正”。
【六龙舞】：众生流转生死，是六根，安乐涅槃，也是六根。背觉合尘名为“倒”，而实无减；背尘合觉名为“正”，而实无增。就如六龙舞，不过是首尾相换而已。",
