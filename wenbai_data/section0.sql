,四十二章经-43,5,视大千界，如一诃子。,看大千世界，就像一个微小的芥子一样。,"【大千界】：为古代印度人之宇宙观。古代印度人以四大洲及日月诸天为一小世界，合一千小世界为小千世界；合一千小千世界为中千世界；合一千中千世界为大千世界。今之俗语乃袭用佛教“大千世界”一词，转用于形容人间之纷纭诸相。小千、中千、大千并提，则称“三千大千世界”。
【诃子】：就是芥子，原系芥菜之种子，颜色有白、黄、赤、青、黑之分，体积微小，故于经典中屡用以比喻极小之物，如谓“芥子容须弥，毛孔收刹海”即为常见于佛典中之譬喻。又因芥子与针锋均为极微小之物，而以“芥子投针锋”比喻极难得之事，如《北本涅槃经》卷二谓，佛出世之难得犹如芥子投针锋。",
,四十二章经-43,6,视阿耨池水，如涂足油。,看众江河源头的雪山水，就像涂足的油一样。,【阿耨池】：“阿耨达池”的简称，“无热”的意思，相传为阎浮提四大河之发源地。又作“阿耨大泉”、“阿那达池”、“阿那婆答多池”、“阿那婆踏池”。略称“阿耨”。意译“清凉池”、“无热恼池”。据《大毗婆沙论》卷十五与《俱舍论》卷十一所载，此池位于大雪山之北，香醉山以南，称为“无热恼池”，周围凡八百里，以金、银、琉璃、颇梨等四宝装饰岸边，其池金沙弥漫，清波皎镜，有龙王居之，名为阿耨达，池中能出清泠之水。池东为恒河出口，南为信度河，西为缚刍河，北为徙多河。,
